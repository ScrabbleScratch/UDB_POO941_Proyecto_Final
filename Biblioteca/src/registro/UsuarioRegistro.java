    private void btnIngresarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarUActionPerformed
        int tipo = 0;
        
        if (cbTipoU.getSelectedItem().toString().equals("Administrador"))
            tipo = 1;
        if (cbTipoU.getSelectedItem().toString().equals("Profesor"))
            tipo = 2;
        if (cbTipoU.getSelectedItem().toString().equals("Estudiante"))
            tipo = 3;
        
        if (Registro.usuario(txtNombreU, txtContraU, tipo))
            goBack();
    }//GEN-LAST:event_btnIngresarUActionPerformed

